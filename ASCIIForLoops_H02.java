public class ASCIIForLoops_H02 {
   public static void main(String[] args) {
        System.out.println("#\tchar");
   
       for (int i = 32; i <= 128; ++i) {

         System.out.println(i + "\t" + (char)i);

      
      
      }//end for loop
   }//end method
}//end class