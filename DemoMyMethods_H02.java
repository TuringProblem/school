public class DemoMyMethods_H02 {
   public static void main(String[] args) {
   //Overloading methods :) using public class MyMathMethods
   System.out.println("My Amount: " + MyMathMethods.mult(2, 6));
   System.out.println("My Amount: " + MyMathMethods.mult(2, 6, 5));
   System.out.println("My Amount: " + MyMathMethods.mult(2.2, 5.5));
   System.out.println("My Amount: " + MyMathMethods.mult(2.2, 5.5, 6.6));
   System.out.println("My Amount: " + MyMathMethods.add(1, 5.5));
   System.out.println("My Amount: " + MyMathMethods.add(2.2, 7));   
   System.out.println("My Amount: " + MyMathMethods.add(8, 7)); 
   }//end main method
}//end class